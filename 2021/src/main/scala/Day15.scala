case class Point (x: Int, y: Int)

def cellValue (p: List[String])(c: Point) : Int =
  if (0<=c.x) && (c.x < p(0).length) && (0 <= c.y) && (c.y < p.length) then
    p(c.y)(c.x).asDigit
  else
    Int.MaxValue

def findPathMinimumCost (p: List[String], start: Point, end: Point) : Int =
  def neighbours (c: Point): List[Point] = List(Point(c.x-1, c.y), Point(c.x+1, c.y), Point(c.x, c.y-1), Point(c.x, c.y+1))
  def forwardNeighbouts (c: Point): List[Point] = List(Point(c.x+1, c.y), Point(c.x, c.y+1))

  val minPathCostForCell: Map[Point, Int] =
    def backtrack (c: Point, environment: Map[Point, Int]): Map[Point, Int] =
      neighbours(c).foldLeft(environment)((a, cc) => {
        val costThroughC = (a.get(c).getOrElse(Int.MaxValue) + cellValue(p)(cc))
        if a.get(cc).getOrElse(Int.MinValue) > costThroughC then
          backtrack(cc, a + (cc -> costThroughC))
        else
          a
      })

    def explore (c: Point, environment: Map[Point, Int]): Map[Point, Int] =
      val forwardExplore = forwardNeighbouts(c).foldLeft(environment)((a, cc) => {
        val ccCost = cellValue(p)(cc)
        if a.get(cc).isEmpty && (ccCost < Int.MaxValue) then
          explore(cc, a + (cc -> (a.get(c).get + ccCost)))
        else
          a
      })

      backtrack(c, forwardExplore)

    explore(start, Map(start -> 0))

  minPathCostForCell.get(end).getOrElse(Int.MaxValue)


private def expandEnvironment (p: List[String], times: Int) : List[String] =
  def incrementChar (times: Int)(c: Char): Char =
    if times == 0 then
      c
    else
      val cc = c match
        case '1' => '2'
        case '2' => '3'
        case '3' => '4'
        case '4' => '5'
        case '5' => '6'
        case '6' => '7'
        case '7' => '8'
        case '8' => '9'
        case '9' => '1'
        case  _  => ' '
      incrementChar(times - 1)(cc)

  def incrementString (times: Int)(s: String): String = s.map(incrementChar(times))

  val orizontalExpansion = p.map(s => {
    Range(1, times).foldLeft(s)((a, i) => a + incrementString(i)(s))
  })

